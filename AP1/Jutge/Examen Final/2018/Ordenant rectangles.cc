#include <iostream>
using namespace std;

int main()
{
    int n;
    while (cin >> n) {
        int a, b;
        int i = 0;
        while (cin >> a >> b and i < n) {
            vector<vector<double>> v(n);
            v[0][i] = a;
            v[1][i] = b;
            ++i;
        }
    }
}
